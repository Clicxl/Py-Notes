
# Intergers
print(-2)
print(type(-5))

# Floats / Decimals
print(1.1)
print(type(2.78))
print(type(1.0)) # Float

# String 
print('Hello World')
print(type('Hello world'))
print("I dont't like you")

# Lists (Can be modified)
print(['a', 'b', 'c']) 
# Lists can have anything in em 
print(['a', 123 , -3 , 0.5])
# Lists can obtain values of each list index
print(['a', 123 , -3 , 0.5][3])

# Tuples (Cannot be changed)
print((1,2,3))

# Dictionary ('key' : value) 
print({'a' : 1 , 'b' : 2, "c" : 3}['b'])





